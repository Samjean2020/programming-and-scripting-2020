# This is a program that takes asks a user to input a string and
# outputs every second letter in reverse order.

# provide a text sequence type: string. 
s = str(input("Please enter a sentence: "))

# the length of the string is:44. Obtained using built-in function len() function.
# start from end of the string and output every second letter.
print(s[-1:-44:-2])











